package labs.lab_01;

public class RectangleAreaCalculator {
    public static void main(String[] args) {
        // Predefined values
        double length = 4.0; // inches
        double width = 8.0;  // inches

        // Calculate the area of the rectangle
        double area = length * width;

        // Output the result
        System.out.println("Length of the rectangle: " + length + " inches");
        System.out.println("Width of the rectangle: " + width + " inches");
        System.out.println("Area of the rectangle: " + area + " square inches");
    }
}
