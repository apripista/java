package labs.lab_04;

public class SumOfCountingIntegers {

    public static void main(String[] args) {
        int sum = 0;
        for(int i = 0; i <=200; i++){
            sum += i;
        }
        System.out.println("sum of first 200 counting number is:");
        System.out.println("sum:  " + sum);

    }
}